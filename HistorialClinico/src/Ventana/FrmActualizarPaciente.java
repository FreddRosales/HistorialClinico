    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
    if (pacienteEncontrado == null) {
            lblMensaje.setForeground(java.awt.Color.RED);
            lblMensaje.setText("Primero busque un paciente válido.");
            return;
        }

        // REQ-29: confirmación antes de guardar
        int confirmacion = javax.swing.JOptionPane.showConfirmDialog(
            this,
            "¿Está seguro de que desea actualizar los datos del paciente?",
            "Confirmar actualización",
            javax.swing.JOptionPane.YES_NO_OPTION
        );

        if (confirmacion != javax.swing.JOptionPane.YES_OPTION) {
            return;
        }

        try {
            // REQ-27: actualizar
            pacienteEncontrado.setDireccion(txtDireccion.getText().trim());
            pacienteEncontrado.setTelefono(txtTelefono.getText().trim());
            pacienteEncontrado.setEdad(Integer.parseInt(txtEdad.getText().trim()));
            pacienteEncontrado.setMaterno(txtMaterno.getText().trim());
            pacienteEncontrado.setPaterno(txtPaterno.getText().trim());
            pacienteEncontrado.setNombre(txtNombre.getText().trim());

            // REQ-30: mensaje de éxito
            lblMensaje.setForeground(new java.awt.Color(0, 128, 0));
            lblMensaje.setText("Datos del paciente actualizados correctamente.");

        } catch (IllegalArgumentException e) {
            lblMensaje.setForeground(java.awt.Color.RED);
            lblMensaje.setText(e.getMessage());
        }
    }//GEN-LAST:event_btnGuardarActionPerformed
    private void cargarDatosPaciente() {
        // REQ-28: campos no editables, solo informativos
        txtHC.setText(String.valueOf(pacienteEncontrado.getNumHC()));
        txtTipoDoc.setText(pacienteEncontrado.getTipodoc());
        txtNumDoc.setText(pacienteEncontrado.getNumdoc());
        lblNombre.setText("Paciente: " + pacienteEncontrado.getNombreCompleto());

        // REQ-27: campos editables
        txtDireccion.setText(pacienteEncontrado.getDireccion());
        txtTelefono.setText(pacienteEncontrado.getTelefono());
        txtNombre.setText(pacienteEncontrado.getNombre());
        txtPaterno.setText(pacienteEncontrado.getPaterno());
        txtMaterno.setText(pacienteEncontrado.getMaterno());
        txtEdad.setText(String.valueOf(pacienteEncontrado.getEdad()));
        
    }

