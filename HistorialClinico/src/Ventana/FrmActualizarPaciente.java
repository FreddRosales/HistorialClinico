    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
    // Busca al paciente y carga sus datos en el formulario
    String dni = txtBuscarNumDoc.getText().trim();

        if (dni.isEmpty()) {
            lblMensaje.setForeground(java.awt.Color.RED);
            lblMensaje.setText("Ingrese un DNI para buscar.");
            return;
        }

        pacienteEncontrado = controladorPaciente.buscarPorDNI(dni);

        if (pacienteEncontrado == null) {
            lblMensaje.setForeground(java.awt.Color.RED);
            lblMensaje.setText("No se encontró ningún paciente con ese criterio de búsqueda.");
            habilitarFormulario(false);
            limpiarFormulario();
            return;
        }

        cargarDatosPaciente();
        habilitarFormulario(true);
        lblMensaje.setForeground(new java.awt.Color(0, 128, 0));
        lblMensaje.setText("Paciente encontrado.");
    }//GEN-LAST:event_btnBuscarActionPerformed

