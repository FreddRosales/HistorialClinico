    private void btnCalcularIMCActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularIMCActionPerformed
    try {
        double peso = Double.parseDouble(txtPeso.getText().trim());
        double talla = Double.parseDouble(txtTalla.getText().trim());

        if (peso <= 0 || talla <= 0) {
            lblMensaje.setForeground(java.awt.Color.RED);
            lblMensaje.setText("Peso y talla deben ser mayores a 0.");
            return;
        }

        double tallaMetros = talla / 100.0;
        double imc = peso / (tallaMetros * tallaMetros);

        txtIMC.setText(String.format("%.2f", imc));
        lblMensaje.setForeground(new java.awt.Color(0, 128, 0));
        lblMensaje.setText("IMC calculado correctamente.");

    } catch (NumberFormatException e) {
        lblMensaje.setForeground(java.awt.Color.RED);
        lblMensaje.setText("Ingrese valores numéricos válidos en Peso y Talla.");
    }
    }//GEN-LAST:event_btnCalcularIMCActionPerformed

    private void btnBuscarDNIActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarDNIActionPerformed
        String dni = txtBuscarDNI.getText().trim();

    if (dni.isEmpty()) {
        lblMensaje.setForeground(java.awt.Color.RED);
        lblMensaje.setText("Ingrese un DNI para buscar.");
        return;
    }

    pacienteEncontrado = controladorPaciente.buscarPorDNI(dni);

    if (pacienteEncontrado != null) {
        lblNombrePaciente.setText("Paciente: " + pacienteEncontrado.getNombreCompleto()
                + "  |  N° HC: " + pacienteEncontrado.getNumHC());
        lblMensaje.setForeground(new java.awt.Color(0, 128, 0));
        lblMensaje.setText("Paciente encontrado. Complete los datos de la consulta.");
        habilitarFormularioConsulta(true);
    } else {
        lblNombrePaciente.setText("");
        lblMensaje.setForeground(java.awt.Color.RED);
        lblMensaje.setText("No se encontró ningún paciente con ese DNI.");
        habilitarFormularioConsulta(false);
        pacienteEncontrado = null;
    }
    }//GEN-LAST:event_btnBuscarDNIActionPerformed

